export function parseTTC(s) {
  if (typeof s === "number") return s;
  if (!s) return 0;
  const v = parseFloat(String(s).replace(/\s/g, "").replace(/€/g, "").replace(",", "."));
  return isNaN(v) ? 0 : v;
}

export function eur(x) {
  return new Intl.NumberFormat("fr-FR", { minimumFractionDigits: 2, maximumFractionDigits: 2 }).format(x) + " €";
}

function rangeFor(amount) {
  for (const r of RANGES) if (amount >= r.low && amount < r.high) return r;
  return RANGES[RANGES.length - 1];
}

function pay(amount, scheme) {
  const c = COEFFS[scheme];
  return c == null ? null : (amount * c) / 100;
}

// Renvoie un objet structure { creditBail: [...lignes], location: [...lignes], premiers: {10,20,30} }
// Chaque ligne : { dureeSans, sans, duree, p10, p20, p30 }
export function computeFinancement(ttc) {
  const r = rangeFor(ttc);
  const build = (family) => {
    const off = family === "loc" ? LOC_OFFSET : 0;
    const rows = [];
    for (let i = 0; i < 4; i++) {
      rows.push({
        dureeSans: DUR_NO[i],
        sans: pay(ttc, r.base[0] + i + off),
        duree: DUR_YES[i],
        p10: pay(ttc, r.base[10] + i + off),
        p20: pay(ttc, r.base[20] + i + off),
        p30: pay(ttc, r.base[30] + i + off),
      });
    }
    return rows;
  };
  return {
    creditBail: build("cb"),
    location: build("loc"),
    premiers: { 10: ttc * 0.1, 20: ttc * 0.2, 30: ttc * 0.3 },
    tranche: `${r.low.toLocaleString("fr-FR")}–${(r.high - 1).toLocaleString("fr-FR")} €`,
  };
}
